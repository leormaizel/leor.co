        <h1>BookingSuite Website Redesign</h1>
          <article>
            <h2>Background</h2>
            <p>My first project at BookingSuite was a real doozy. Formerly Buuteeq, the company had been acquired by Booking.com 6 months prior, and rebranded as BookingSuite. With no design or development resources, the marketing team launched a temporary website to reflect this rebrand, but it was clear that this site did not reflect our desired image as the aspiring leader in hospitality software. I was brought on to lead this redesign initiative.</p>
            <p>[Old Site]</p>
            <h2>My Process</h2>
            <p>I worked with copywriters and the director of marketing to understand our brand voice––How do we wish to present ourselves to the market? What is our unique value proposition? I then conducted a content audit to map out the content we already have, which was worth keeping, and which was redundant, outdated, and/or trivial. One of the most interesting findings was that we had a treasure trove of client testimonials that were not presented in a compelling or easily accessible manner.</p>
            <p>[Content Audit]</p>
            <p>I then proceeded to sketching and wire framing; I proposed a site map and a basic structure and layout for each page. I frequently gathered feedback from relevant stakeholders and iterated on my designs before moving onto the visual design and development stages. One of the features I proposed was a content hub that better showcased the videos and testimonials we had created over the years, and which could be combined with our under-utilized blog, leading to a much more impactful end product.</p>
            <p>[Wireframes]</p>
            <p>Here’s the doozy––two months into this process, BookingSuite got a new head of Marketing, and acquired another company, planning to integrate it’s product into our own. The site structure, product descriptions, and unique value proposition all needed to be updated.</p>
            <p>I worked with a Graphic Designer to create a style guide, and full graphic mockups. Since we used a contract developer to implement this site in a custom WordPress build, the design process proceeded along more of a waterfall pattern than I’d ideally like. I wrote the dev spec, and worked closely with the contractor to ensure the build matched my initial designs.</p>
            <h2>Lessons Learned</h2>
            <p>Redesigning a website is always a process fraught with politics, as there are so many stakeholders. Some of the organizational chaos I mentioned  could not have been avoided. Rather, if I could re-do this project, I would spend a lot more time discussing front-end implementation choices with the contract agency. My assumptions regarding industry-standard best-practices in responsive design turned out not to be safe assumptions, and as a result, the CMS was not built as flexibly as I’d like.</p>
            <p>As a whole, the site was received by the main stakeholders as a great improvement, and has not significantly changed in over a year.</p>
