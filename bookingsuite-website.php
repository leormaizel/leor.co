        <h2 class="item-title">An Unexpected Change</h2>
        <p>Here’s the doozy–two months into this process, BookingSuite got a new head of Marketing, and acquired another company, planning to integrate it’s product into our own. The site structure, product descriptions, and unique value proposition all needed to be updated.</p>
        <p>I worked with a Graphic Designer to create a style guide, and full graphic mockups. Since we used a contract developer to implement this site in a custom WordPress build, the design process proceeded along more of a waterfall pattern than I’d ideally like. I wrote the dev spec, and worked closely with the contractor to ensure the build matched my initial designs.</p>
